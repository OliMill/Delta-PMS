    private void setupTableCustomization() {
        // We define the model here
        DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "ID", "First Name", "Last Name", "Email", "DOB"
                }
        ) {
            boolean[] canEdit = new boolean[]{
                false, true, true, true, false // ID and DOB usually shouldn't be edited manually
            };

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };

        jTable1.setModel(model);
    }


    //Fetches fresh data from DataManager and populates the table.
    private void loadCustomerTable() {
        DataManager.loadDataFromDatabase(); // Force a refresh from SQL
        allCustomers = DataManager.getCustomers();
        updateTable(allCustomers);
    }


    //Updates the JTable rows with the provided list of customers.

    private void updateTable(List<Customer> customers) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Customer c : customers) {
            model.addRow(new Object[]{
                c.getCustomerID(),
                c.getFirstName(),
                c.getLastName(),
                c.getEmail(),
                c.getDob(),
            });
        }
    }

    //Saves changes from the JTable back to the SQL Database.

    private void updateDatabaseAction() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String sql = "UPDATE Customer SET FirstName = ?, LastName = ?, Email = ? WHERE CustomerID = ?";

        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {

            for (int i = 0; i < model.getRowCount(); i++) {
                int id = Integer.parseInt(model.getValueAt(i, 0).toString());
                String fName = model.getValueAt(i, 1).toString();
                String lName = model.getValueAt(i, 2).toString();
                String email = model.getValueAt(i, 3).toString();

                pstmt.setString(1, fName);
                pstmt.setString(2, lName);
                pstmt.setString(3, email);
                pstmt.setInt(4, id);
                
                pstmt.addBatch();
            }

            pstmt.executeBatch();
            JOptionPane.showMessageDialog(this, "Changes saved to database.");
            loadCustomerTable(); 

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }
    }
