    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String query = jTextField1.getText().toLowerCase().trim();
        List<Customer> filtered = new ArrayList<>();
        
        // Filter the existing list
        for (Customer c : allCustomers) {
            if (c.getFirstName().toLowerCase().contains(query) || 
                c.getLastName().toLowerCase().contains(query) || 
                c.getEmail().toLowerCase().contains(query)) {
                filtered.add(c);
            }
        }
        
        // Update table with only found results
        updateTable(filtered);
        
        if (filtered.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No customers found matching: " + query);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

