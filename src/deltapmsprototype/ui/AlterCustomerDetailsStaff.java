    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Create text fields and a password field
        javax.swing.JTextField fNameField = new javax.swing.JTextField();
        javax.swing.JTextField lNameField = new javax.swing.JTextField();
        javax.swing.JTextField emailField = new javax.swing.JTextField();
        javax.swing.JTextField dobField = new javax.swing.JTextField();
        javax.swing.JPasswordField passField = new javax.swing.JPasswordField(); // Password field

        // Arrange them in a vertical layout
        javax.swing.JPanel panel = new javax.swing.JPanel(new java.awt.GridLayout(0, 1, 5, 5));
        panel.add(new javax.swing.JLabel("First Name:"));
        panel.add(fNameField);
        panel.add(new javax.swing.JLabel("Last Name:"));
        panel.add(lNameField);
        panel.add(new javax.swing.JLabel("Email:"));
        panel.add(emailField);
        panel.add(new javax.swing.JLabel("DOB (YYYY-MM-DD):"));
        panel.add(dobField);
        panel.add(new javax.swing.JLabel("Set Password:"));
        panel.add(passField);

        // Show the Popup
        int result = JOptionPane.showConfirmDialog(this, panel, "Add New Customer",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (result == JOptionPane.OK_OPTION) {
            String fName = fNameField.getText().trim();
            String lName = lNameField.getText().trim();
            String email = emailField.getText().trim();
            String dob = dobField.getText().trim();
            String rawPassword = new String(passField.getPassword());

            // Basic Validation
            if (fName.isEmpty() || email.isEmpty() || rawPassword.isEmpty()) {
                JOptionPane.showMessageDialog(this, "First Name, Email, and Password are required.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String r = PasswordHasher.securePassword(rawPassword);
            if (!r.equals("")) {
                // This will pop up a window showing ALL missing requirements
                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        result,
                        "Security Requirements",
                        javax.swing.JOptionPane.ERROR_MESSAGE
                );
            } else {
                String hashedPass = rawPassword;
                // Database Insertion
                String sql = "INSERT INTO Customer (FirstName, LastName, DOB, Email, PasswordHash) VALUES (?, ?, ?, ?, ?)";

                try (java.sql.Connection conn = com.hotelmanagement.dao.DatabaseConnection.getConnection(); java.sql.PreparedStatement pstmt = conn.prepareStatement(sql)) {

                    pstmt.setString(1, fName);
                    pstmt.setString(2, lName);
                    pstmt.setString(3, dob);
                    pstmt.setString(4, email);
                    pstmt.setString(5, hashedPass);

                    pstmt.executeUpdate();

                    JOptionPane.showMessageDialog(this, "Customer " + fName + " added successfully!");

                    //Refresh the main table
                    loadCustomerTable();

                } catch (java.sql.SQLException e) {
                    // Check for specific errors like duplicate emails
                    if (e.getMessage().contains("UNIQUE") || e.getMessage().contains("Duplicate")) {
                        JOptionPane.showMessageDialog(this, "Error: This email is already registered.", "Database Error", JOptionPane.ERROR_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Get the selected row
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a customer from the table first.");
            return;
        }

        // Get the ID and Name from the table for the warning message
        String customerID = jTable1.getValueAt(selectedRow, 0).toString();
        String customerName = jTable1.getValueAt(selectedRow, 1).toString() + " " + jTable1.getValueAt(selectedRow, 2).toString();

        // The Warning Message
        int confirm = JOptionPane.showConfirmDialog(this,
                "WARNING: Deleting '" + customerName + "' will also PERMANENTLY DELETE all their bookings.\n"
                + "Are you sure you want to proceed?",
                "Confirm Deletion",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            deleteCustomerFromDB(customerID);
        }

    }//GEN-LAST:event_jButton5ActionPerformed

    private void deleteCustomerFromDB(String id) {
        String sql = "DELETE FROM Customer WHERE CustomerID = ?";

        try (java.sql.Connection conn = com.hotelmanagement.dao.DatabaseConnection.getConnection(); java.sql.PreparedStatement pstmt = conn.prepareStatement(sql)) {

            pstmt.setString(1, id);
            pstmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Customer and associated bookings removed.");

            // Refresh the table to show they are gone
            loadCustomerTable();

        } catch (java.sql.SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting customer: " + e.getMessage());
        }
    }
