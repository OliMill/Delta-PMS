    private void setupTableCustomization() {
        DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "ID", "First Name", "Last Name", "Email", "Role", "DOB"
                }
        ) {
            boolean[] canEdit = new boolean[]{
                false, true, true, true, true, false // ID and DOB locked
            };

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };
        jTable1.setModel(model);

        // Create the dropdown for the table cell
        String[] roles = {"Staff", "Manager"};
        javax.swing.JComboBox<String> roleEditor = new javax.swing.JComboBox<>(roles);

        // Assign the dropdown to the "Role" column (Index 4)
        // This replaces the default text field with a JComboBox
        jTable1.getColumnModel().getColumn(4).setCellEditor(new javax.swing.DefaultCellEditor(roleEditor));
    }

    //Fetches fresh data from DataManager and populates the table.
    private void loadStaffTable() {
        DataManager.loadDataFromDatabase();
        allStaff = DataManager.getStaff();
        updateTable(allStaff);
    }


    //Updates the JTable rows with the provided list of customers.

    private void updateTable(List<Staff> staffList) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 

        for (Staff s : staffList) {
            model.addRow(new Object[]{
                s.getStaffID(),
                s.getFirstName(),
                s.getLastName(),
                s.getEmail(),
                s.getRole(), // Added Role column
                s.getDob()
            });
        }
    }

    //Saves changes from the JTable back to the SQL Database.

    private void updateDatabaseAction() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // Updated SQL for Staff table
        String sql = "UPDATE Staff SET FirstName = ?, LastName = ?, Email = ?, Role = ? WHERE StaffID = ?";

        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {

            for (int i = 0; i < model.getRowCount(); i++) {
                int id = Integer.parseInt(model.getValueAt(i, 0).toString());
                String fName = model.getValueAt(i, 1).toString();
                String lName = model.getValueAt(i, 2).toString();
                String email = model.getValueAt(i, 3).toString();
                String role = model.getValueAt(i, 4).toString();

                pstmt.setString(1, fName);
                pstmt.setString(2, lName);
                pstmt.setString(3, email);
                pstmt.setString(4, role);
                pstmt.setInt(5, id);
                
                pstmt.addBatch();
            }

            pstmt.executeBatch();
            JOptionPane.showMessageDialog(this, "Staff changes saved successfully.");
            loadStaffTable(); 

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }
    }
