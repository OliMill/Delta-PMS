    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        
        
        //Main difference from customer version here
        
        javax.swing.JTextField fNameField = new javax.swing.JTextField();
        javax.swing.JTextField lNameField = new javax.swing.JTextField();
        javax.swing.JTextField emailField = new javax.swing.JTextField();
        javax.swing.JTextField dobField = new javax.swing.JTextField();
        javax.swing.JPasswordField passField = new javax.swing.JPasswordField();

        // Create the Dropdown (JComboBox) for roles
        String[] roles = {"Staff", "Manager"};
        javax.swing.JComboBox<String> roleDropdown = new javax.swing.JComboBox<>(roles);

        // Arrange them in a vertical layout
        javax.swing.JPanel panel = new javax.swing.JPanel(new java.awt.GridLayout(0, 1, 5, 5));
        panel.add(new javax.swing.JLabel("First Name:"));
        panel.add(fNameField);
        panel.add(new javax.swing.JLabel("Last Name:"));
        panel.add(lNameField);
        panel.add(new javax.swing.JLabel("Email:"));
        panel.add(emailField);
        panel.add(new javax.swing.JLabel("Role:"));
        panel.add(roleDropdown); // Add the dropdown instead of a text field
        panel.add(new javax.swing.JLabel("DOB (YYYY-MM-DD):"));
        panel.add(dobField);
        panel.add(new javax.swing.JLabel("Set Password:"));
        panel.add(passField);

        // Show the Popup
        int result = JOptionPane.showConfirmDialog(this, panel, "Add New Staff Member",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (result == JOptionPane.OK_OPTION) {
            String fName = fNameField.getText().trim();
            String lName = lNameField.getText().trim();
            String email = emailField.getText().trim();
            String selectedRole = (String) roleDropdown.getSelectedItem(); // Get selection
            String dob = dobField.getText().trim();
            String rawPassword = new String(passField.getPassword());

            // Basic Validation
            if (fName.isEmpty() || email.isEmpty() || rawPassword.isEmpty()) {
                JOptionPane.showMessageDialog(this, "First Name, Email, and Password are required.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String r = PasswordHasher.securePassword(rawPassword);

            if (!r.equals("")) {
                // This will pop up a window showing ALL missing requirements
                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        result,
                        "Security Requirements",
                        javax.swing.JOptionPane.ERROR_MESSAGE
                );
            } else {
                // Success: Proceed to Hash and Save
                // Database Insertion
                String sql = "INSERT INTO Staff (FirstName, LastName, DOB, Email, Role, PasswordHash) VALUES (?, ?, ?, ?, ?, ?)";

                try (java.sql.Connection conn = com.hotelmanagement.dao.DatabaseConnection.getConnection(); java.sql.PreparedStatement pstmt = conn.prepareStatement(sql)) {

                    pstmt.setString(1, fName);
                    pstmt.setString(2, lName);
                    pstmt.setString(3, dob);
                    pstmt.setString(4, email);
                    pstmt.setString(5, selectedRole); // "Manager" or "Staff"
                    pstmt.setString(6, PasswordHasher.hashPassword(rawPassword));

                    pstmt.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Staff member " + fName + " added successfully!");
                    loadStaffTable();

                } catch (java.sql.SQLException e) {
                    JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Get the selected row
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a staff member first.");
            return;
        }

        String staffID = jTable1.getValueAt(selectedRow, 0).toString();
        String staffName = jTable1.getValueAt(selectedRow, 1).toString();

        int confirm = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete staff member: " + staffName + "?",
            "Confirm Deletion", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            deleteStaffFromDB(staffID);
        }
    }//GEN-LAST:event_jButton5ActionPerformed
    
    private void deleteStaffFromDB(String id) {
        String sql = "DELETE FROM Staff WHERE StaffID = ?";
        try (Connection conn = DatabaseConnection.getConnection(); 
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, id);
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Staff member removed.");
            loadStaffTable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting staff: " + e.getMessage());
        }
    }


