    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String query = jTextField1.getText().toLowerCase().trim();
        List<Staff> filtered = new ArrayList<>();

        for (Staff s : allStaff) {
            if (s.getFirstName().toLowerCase().contains(query)
                    || s.getLastName().toLowerCase().contains(query)
                    || s.getEmail().toLowerCase().contains(query)
                    || s.getRole().toLowerCase().contains(query)) { // Included role in search
                filtered.add(s);
            }
        }
        updateTable(filtered);
    }//GEN-LAST:event_jButton2ActionPerformed

